            <h3>
                Getting your Service Account Key
            </h3>
    <p>
        The first thing you’ll need to do is to get some credentials to use Google API’s. I’m assuming that you’ve
        already created a Google account and are signed in. Head over to <a
                href="https://console.developers.google.com/apis">Google API’s site</a> and click "Select a project" in
        the header.
    </p>
    <p>
        <img style="max-width: 100%;"
             src="https://camo.githubusercontent.com/18c37bf7ffcf40136c0647b1ce903dc19d1f0263/68747470733a2f2f7370617469652e6769746875622e696f2f6c61726176656c2d616e616c79746963732f76322f312e6a7067">
    </p>
    <p>
        Next up we must specify which API’s the project may consume. In the list of available API’s click "Google
        Analytics API". On the next screen click "Enable".</p>
    <p>
        <img style="max-width: 100%;"
             src="https://camo.githubusercontent.com/7d9e57d5e273f608118fc3402fce5c98b65db72a/68747470733a2f2f7370617469652e6769746875622e696f2f6c61726176656c2d616e616c79746963732f76322f322e6a7067">
    </p>
    <p>
        Now that you’ve created a project that has access to the Analytics API it’s time to download a file with these
        credentials. Click "Credentials" in the sidebar. You’ll want to create a "Service account key".</p>
    <p>
        <img style="max-width: 100%;"
             src="https://camo.githubusercontent.com/f780fa641b053d84c611f4fad536a12021b954a8/68747470733a2f2f7370617469652e6769746875622e696f2f6c61726176656c2d616e616c79746963732f76322f332e6a7067">
